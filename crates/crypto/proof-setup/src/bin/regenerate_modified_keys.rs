use std::fs;
use std::path::{Path, PathBuf};

use anyhow::Context;
use ark_serialize::CanonicalSerialize;
use penumbra_sdk_dex::{swap::proof::SwapCircuit, swap_claim::proof::SwapClaimCircuit};
use penumbra_sdk_governance::DelegatorVoteCircuit;
use penumbra_sdk_proof_params::{generate_test_parameters, DummyWitness, ProvingKeyExt};
use penumbra_sdk_shielded_pool::{
    ConvertCircuit, NullifierDerivationCircuit, OutputCircuit, SpendCircuit,
};
use rand_chacha::ChaCha20Rng;
use rand_core::SeedableRng;

fn write_params<T: DummyWitness>(name: &str, gen_dir: &Path, seed: u64) -> anyhow::Result<()> {
    let mut rng = ChaCha20Rng::seed_from_u64(seed);
    let (pk, vk) = generate_test_parameters::<T>(&mut rng);

    let mut pk_bytes = Vec::new();
    pk.serialize_uncompressed(&mut pk_bytes)
        .context("serializing proving key")?;
    fs::write(gen_dir.join(format!("{name}_pk.bin")), pk_bytes)
        .with_context(|| format!("writing {name}_pk.bin"))?;

    let mut vk_bytes = Vec::new();
    vk.serialize_uncompressed(&mut vk_bytes)
        .context("serializing verifying key")?;
    fs::write(gen_dir.join(format!("{name}_vk.param")), vk_bytes)
        .with_context(|| format!("writing {name}_vk.param"))?;

    let id_path = gen_dir.join(format!("{name}_id.rs"));
    let id_contents = format!(
        "// AUTOGENERATED by penumbra-sdk-proof-setup regenerate_modified_keys\n\
pub const PROVING_KEY_ID: &'static str = \"{}\";\n",
        pk.debug_id()
    );
    fs::write(&id_path, id_contents).with_context(|| format!("writing {}", id_path.display()))?;

    Ok(())
}

fn main() -> anyhow::Result<()> {
    let manifest_dir = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
    let gen_dir = manifest_dir
        .join("../proof-params/src/gen")
        .canonicalize()
        .context("resolving proof-params gen directory")?;

    write_params::<SpendCircuit>("spend", &gen_dir, 0x2026_0323_0001)?;
    write_params::<OutputCircuit>("output", &gen_dir, 0x2026_0323_0002)?;
    write_params::<SwapCircuit>("swap", &gen_dir, 0x2026_0305_0001)?;
    write_params::<SwapClaimCircuit>("swapclaim", &gen_dir, 0x2026_0305_0002)?;
    write_params::<ConvertCircuit>("convert", &gen_dir, 0x2026_0305_0003)?;
    write_params::<DelegatorVoteCircuit>("delegator_vote", &gen_dir, 0x2026_0305_0004)?;
    write_params::<NullifierDerivationCircuit>("nullifier_derivation", &gen_dir, 0x2026_0305_0005)?;

    Ok(())
}
